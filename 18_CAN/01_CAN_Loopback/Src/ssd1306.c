void SSD1306_Init(void)
{
    SSD1306_WriteCommand(0xAE);     /* Display OFF */

    SSD1306_WriteCommand(0xD5);     /* Set display clock divide ratio */
    SSD1306_WriteCommand(0x80);     /* Suggested ratio */

    SSD1306_WriteCommand(0xA8);     /* Set multiplex ratio */
    SSD1306_WriteCommand(0x3F);     /* 1/64 duty */

    SSD1306_WriteCommand(0xD3);     /* Set display offset */
    SSD1306_WriteCommand(0x00);     /* No offset */

    SSD1306_WriteCommand(0x40);     /* Set display start line = 0 */

    SSD1306_WriteCommand(0x8D);     /* Charge pump setting */
    SSD1306_WriteCommand(0x14);     /* Enable charge pump */

    SSD1306_WriteCommand(0x20);     /* Memory addressing mode */
    SSD1306_WriteCommand(0x00);     /* Horizontal addressing mode */

    SSD1306_WriteCommand(0xA1);     /* Segment remap */

    SSD1306_WriteCommand(0xC8);     /* COM output scan direction. Scan COM lines from bottom to top.*/

    SSD1306_WriteCommand(0xDA);     /* COM pins hardware configuration */
    SSD1306_WriteCommand(0x12);

    SSD1306_WriteCommand(0x81);     /* Set contrast */
    SSD1306_WriteCommand(0xCF);

    SSD1306_WriteCommand(0xD9);     /* Set pre-charge period */
    SSD1306_WriteCommand(0xF1);

    SSD1306_WriteCommand(0xDB);     /* Set VCOMH deselect level */
    SSD1306_WriteCommand(0x40);

    SSD1306_WriteCommand(0xA4);     /* Display follows RAM contents */

    SSD1306_WriteCommand(0xA6);     /* Normal display */
    
    SSD1306_Clear();                /* Clear display */
    
    SSD1306_WriteCommand(0xAF);     /* Display ON */
}

void SSD1306_SetCursor(uint8_t page, uint8_t column)
{
    SSD1306_WriteCommand(0x21);     /* Set column address */
    SSD1306_WriteCommand(column);   /* Column start */
    SSD1306_WriteCommand(127);      /* Column end */

    SSD1306_WriteCommand(0x22);     /* Set page address */
    SSD1306_WriteCommand(page);     /* Page start */
    SSD1306_WriteCommand(7);        /* Page end */
}

void SSD1306_WriteCommand(uint8_t cmd)
{
    I2C1_Start();                                               /* Generate START condition */
    I2C1_SendAddress(SSD1306_I2C_ADDRESS, I2C_WRITE);           /* Send SSD1306 address with write bit */
    I2C1_WriteByte(SSD1306_COMMAND);                            /* Send command control byte (0x00) */
    I2C1_WriteByte(cmd);                                        /* Send command */
    I2C1_Stop();                                                /* Generate STOP condition */
}

void SSD1306_WriteData(uint8_t data)
{
    I2C1_Start();                                               /* Generate START condition */
    I2C1_SendAddress(SSD1306_I2C_ADDRESS, I2C_WRITE);           /* Send SSD1306 address with write bit */
    I2C1_WriteByte(SSD1306_DATA);                               /* Send data control byte (0x40) */
    I2C1_WriteByte(data);                                       /* Send data */
    I2C1_Stop();                                                /* Generate STOP condition */
}

void SSD1306_WriteBuffer(const uint8_t *buffer, uint16_t length)
{
    uint16_t i;

    if ((buffer == NULL) || (length == 0U))
    {
        return;
    }

    I2C1_Start();                                                   /* Generate START condition */
    I2C1_SendAddress(SSD1306_I2C_ADDRESS, I2C_WRITE);               /* Send SSD1306 address with write bit */
    I2C1_WriteByte(SSD1306_DATA);                                   /* Send data control byte (0x40) */

    for (i = 0U; i < length; i++)
    {
        I2C1_WriteByte(buffer[i]);                                  /* Send display data */
    }

    I2C1_Stop();                                                    /* Generate STOP condition */
}

void SSD1306_Clear(void)
{
    static const uint8_t SSD1306_Buffer[1024] = {0};

    SSD1306_WriteCommand(0x21);                                     /* Set column address */
    SSD1306_WriteCommand(0x00);                                     /* Column start */
    SSD1306_WriteCommand(0x7F);                                     /* Column end */

    SSD1306_WriteCommand(0x22);                                     /* Set page address */
    SSD1306_WriteCommand(0x00);                                     /* Page start */
    SSD1306_WriteCommand(0x07);                                     /* Page end */

    SSD1306_WriteBuffer(SSD1306_Buffer, sizeof(SSD1306_Buffer));
}

void SSD1306_WriteChar(char ch)
{
    uint8_t i;

    if ((ch < 32) || (ch > 126))
    {
        ch = '?';
    }

    for (i = 0U; i < 5U; i++)
    {
        SSD1306_WriteData(Font5x7[ch - 32][i]);
    }

    SSD1306_WriteData(0x00);                        /* One blank column between characters */
}

void SSD1306_WriteString(const char *str)
{
    while (*str != '\0')
    {
        SSD1306_WriteChar(*str++);
    }
}
